
public class Circulo {
	
	double radio = 177808;
	double area;
	double perimetro;

	public double circunferencia () {
		return perimetro = (2*3.1416)*radio;
	}
	public double area (){ 
		return area = 3.1416*Math.pow(radio, 2);
	}
	public double cambiaRadio(double radio) {
		return this.radio = radio;
	}
	
}
